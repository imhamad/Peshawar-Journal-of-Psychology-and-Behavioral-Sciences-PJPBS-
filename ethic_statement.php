        <h4><b> Ethics Statement</b></h4>          </div>
          <div class="art_content2">

<p>The editors of Peshawar Journal of  Psychology and Behavioral Sciences (PJPBS) place high value on integrity in  research publication. We consider it an essential part of our ethical  responsibilities as editors to be aware of challenges posed by plagiarism,  fraud and misconduct in academic publishing and to develop strategies for  coping with them. We expect the same awareness of our journal s peer reviewers.<br>
  The editors of PJPBS do their best  to ensure fair, unbiased, and transparent peer review processes and editorial  decisions. The PJPBS follows a strict code of conduct which is to be followed  by all the parties associated with the publication process, including the  authors, the editors, the editorial board, the peer reviewers, the publisher  and the readers. This code of conduct is derived from the Committee on  Publication Ethics (COPE) Code of Conduct guidelines which can be found <a href="http://www.publicationethics.org">here</a>.  The PJPBS Editors, the contributing authors and the reviewers have the  principal responsibilities. Follow relevant links to peruse details.</p>
<p><a href="http://icp.edu.pk/images/notic_board/respos_pjpbs_editor.pdf">Responsibilities of Editors</a><br>
  <a href="http://icp.edu.pk/images/notic_board/respos_pjpbs_author.pdf">Responsibilities of Authors</a><br>
  <a href="http://icp.edu.pk/images/notic_board/respos_pjpbs_revier.pdf">Responsibilities of Reviewers</a><br>
  <br>
Any detected cases of misconduct,  whether on the part of authors, reviewers, editors, or publishers will be  vigorously pursued.</p>
