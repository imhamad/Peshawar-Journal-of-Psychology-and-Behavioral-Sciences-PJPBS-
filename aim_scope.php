          <p><em>Peshawar Journal of  Psychology and Behavioral Sciences (PJPBS) is open access Journal, published biannually by the  Department of Psychology, Islamia College Peshawar.</em> It publishes original empirical articles covering diverse facets  of Psychology and Behavioral Sciences. It is intended to be a forum for  generating discourse that blends science and theory and ultimately evolves into  practice and policy. PJPBS publishes research from wide range of areas  including</p>
<ul>
  <li>Clinical Psychology</li>
  <li>Counseling Psychology</li>
  <li>Positive Psychology</li>
  <li>Community Psychology</li>
  <li>Developmental Psychology</li>
  <li>Psychology of Gender</li>
  <li>Forensic Psychology</li>
  <li>Military Psychology</li>
  <li>Trauma Psychology</li>
  <li>Behavioral Analysis</li>
  <li>Sexuality</li>
  <li>Social Psychology</li>
  <li>Sport Psychology</li>
  <li>Educational Psychology</li>
  <li>Experimental Psychology</li>
  <li>Test Construction</li>
  <li>Social Psychology</li>
  <li>Neuropsychology</li>
  <li>Cultural Psychology</li>
  <li>Media Psychology</li>
  <li>Consumer Behavior</li>
  <li>Organizational Psychology</li>
  <li>Cyber Psychology</li>
  <li>Tele Mental Health</li>
</ul>
<p>PJPBS looks forward  to receiving research from all corners but is specifically interested in  comparative research, research from South Asian or other collectivistic  cultures. Such research not only captures diversity of human experience but is  culturally relevant. PJPBS provides niche for the areas that are under-represented  in International publications but have implications for local population.<br>
  The journal publishes articles that use  experimental and correlational methods and qualitative analyses, if applicable.<br>
  All research reports should reflect  methodologically rigorous designs.</p>
