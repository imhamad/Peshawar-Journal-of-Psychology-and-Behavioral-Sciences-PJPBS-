<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Publication Policies</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
PJPBS policy prohibits an author from submitting the same manuscript
for concurrent consideration by two or more publications.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
PJPBS requires authors to disclose any possible conflict of interest
in the conduct and reporting of research (e.g., financial interests
in a test or procedure, funding by pharmaceutical companies for drug
research, funding by any NGO).</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Authors are required to provide information on prior dissemination of
the data and narrative interpretations of the data/research appearing
in the manuscript (e.g., if some or all were presented at a
conference or meeting, posted on a listserv, shared on a website,
including academic social networks like Research Gate, etc.). This
information must be briefly (not more than 3-4 sentences) provided in
Author Note.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Ethical Principles</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Data on which manuscript is based should be authentic and original.
(See APA Ethical Principles Standard 8.13).</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
In the best interest of scientific research culture, PJPBS expects
authors to have their data available for at least 5 years after the
date of publication (See APA Ethical Principles Standard 8.14).</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Authors are required to report in procedure section that they have
complied with APA ethical standards in the treatment of their sample,
human or animal, or to describe the details of treatment. Any
deviance must be justified in terms of risk benefit ratio and steps
taken to undo harm must be shared.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Manuscript submission checklist for authors to ensure completion</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<UL>
	<LI><P LANG="en-US" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
	APA style writing <A HREF="http://www.apa.org/pubs/authors/manuscript-check.aspx">checklist</A>
	to ensure adherence to the required format</P>
	<LI><P LANG="en-US" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
	Turnitin Report</P>
	<LI><P LANG="en-US" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
	Signed letter by the author/s stating that manuscript has not been
	presented for publication before or is not being submitted
	concurrently to any other publication source</P>
	<LI><P LANG="en-US" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
	A brief note about the author/s that includes postal and email
	addresses, organizational affiliations, acknowledgements and funding
	related information etc</P>
</UL>
