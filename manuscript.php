        <h4><b> Manuscript Submission</b> </h4>
<P LANG="en-US" CLASS="western" STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Prior to submission, please carefully read and follow the submission
guidelines detailed below. Manuscripts that do not conform to the
submission guidelines may be returned without review.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Submission and Correspondence</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Online submission (doc, docx or pdf files) must be made through the
official website of the journal <A HREF="http://www.pjpbsicp.com">www.pjpbsicp.com</A>,
through online submission system available on the website of the
journal.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Correspondence and submission of manuscripts must be addressed to:
The Editor, Peshawar Journal of Psychology and Behavioral Sciences,
Department of Psychology, Islamia College Peshawar, Peshawar, Khyber
Pakhtunkhwa, Pakistan.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; background: #ffffff; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; background: #ffffff; line-height: 150%">
<FONT COLOR="#111111">Plagiarism must be properly addressed by the
author. As per Higher Education Commission, Pakistan, criteria for
Similarity Index is less than 19%. The Turnitin report must be
attached with manuscript on submission.</FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in; line-height: 150%"><BR>
</P>
<P LANG="en-US" CLASS="western" STYLE="margin-bottom: 0in; line-height: 150%">
<FONT COLOR="#000000"><FONT FACE="Arial, serif"><FONT SIZE=2 STYLE="font-size: 10pt"><SPAN LANG="en-GB"><SPAN STYLE="background: #ffffff">Article
Processing Charges (APCs)</SPAN></SPAN></FONT></FONT></FONT></P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
There are no articles processing charges and no submission fee.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%"><A NAME="_GoBack"></A>
Double Blind Review Policy</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Double blind review policy means that the reviewers of the manuscript
remain anonymous throughout the process of review. Authors are
required not to leave any clue to their identity in the manuscript. 
Authors' names, affiliations, and contact information should be
included only in the cover letter.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Manuscript Preparation</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Manuscript has to be prepared according to the&nbsp;<I>Publication
Manual of the American Psychological Association</I>&nbsp;(6<SUP>th</SUP>edition).
Authors should use <A HREF="http://supp.apa.org/style/pubman-ch03.00.pdf?_ga=1.249758470.1687351413.1462778728">bias-free
language</A> in manuscript (see Chapter 3 of the&nbsp;<I>Publication
Manual</I>).</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
All the formatting instructions, as well as instructions on preparing
tables, figures, references, metrics, and abstracts, should be
followed as given in the&nbsp;<I>Manual</I>. APA Style website may be
accessed for additional guidance on <A HREF="http://flash1r.apa.org/apastyle/basics/index.htm?_ga=1.77355541.1292377061.1452802334">APA
Style</A>.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Tables</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Use Word's Insert Table function for creating tables. Using spaces or
tabs in your table will create problems when the table is typeset and
may result in errors.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
Abstract and Keywords</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
All manuscripts must include an abstract containing a maximum of 200
words typed on a separate page.  Include up to five keywords or brief
phrases after the abstract.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
References</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
List references in alphabetical order. Each listed reference should
be cited in text, and each text citation should be listed in the
References section.</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
<BR>
</P>
<P LANG="en-US" CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0in; line-height: 150%">
